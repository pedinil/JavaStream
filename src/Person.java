
public class Person {
    
    private String name;
    private String Country;
    
    
    public Person (String name,String Country)
    {
	
	this.name=name;
	this.Country=Country;
    }
    
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getCountry() {
        return Country;
    }
    public void setCountry(String country) {
        Country = country;
    }

    
    

}
